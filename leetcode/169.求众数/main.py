class Solution(object):
    """
    给定一个大小为 n 的数组，找到其中的众数。众数是指在数组中出现次数大于 ⌊ n/2 ⌋ 的元素。

你可以假设数组是非空的，并且给定的数组总是存在众数。

示例 1:

输入: [3,2,3]
输出: 3
示例 2:

输入: [2,2,1,1,1,2,2]
输出: 2
    """
    def majorityElement(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        c = 0
        n = -1
        for i, num in enumerate(nums):
            if c == 0:
                n = num
                c = 1
                continue
            if nums[i] != n:
                c -= 1
            else:
                c += 1

        return n


